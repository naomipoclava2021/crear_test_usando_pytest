import re


def suma(x,y):
    return x+y

def resta(x,y):
    return x - y

def multiplicar(x,y):
    return x * y

def dividir(x,y):
    return x / y
    
class Calculadora:
    def sumar(x,y):
        return suma(x,y)
    def resta(x,y):
        return resta(x,y)

    def multiplicar(x,y):
        return multiplicar(x,y)

    def dividir(x,y):
        return dividir(x,y)